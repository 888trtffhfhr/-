package com.example.detailedpager.net;

public class Detailedpage_pics {
	private String url;
	private String detailed_api;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getDetailed_api() {
		return detailed_api;
	}

	public void setDetailed_api(String detailed_api) {
		this.detailed_api = detailed_api;
	}

	@Override
	public String toString() {
		return "Detailedpage_pics [url=" + url + ", detailed_api="
				+ detailed_api + "]";
	}
}
